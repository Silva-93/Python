"""
String - str -> Textos | São digitados dentro de aspas simples '' ou aspas duplas ""

Int -> Números inteiros | Não são digitados dentro de aspas simples ou duplas

Float -> Números "flutuantes" números com vírgula |  Não são digitados dentro de aspas simples ou duplas

Boolean - bool -> Valores lógicos, verdadeiro ou falso
"""

print('Isso é uma String')  # Isso é uma string

print(1, 5, 10)  # Isso é um int

print(1.2, 10.12)  # Isso é um float

print(True)  # Verdadeiro | bool

print(False)  # Falso | bool


#  CONVERTENDO VALORE

print(type(int('1')))  #  Converteu uma str em um int
print(type(str(1)))  #  Converteu um int em uma str

